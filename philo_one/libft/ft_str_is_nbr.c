#include "libft.h"

int	ft_str_is_nbr(char *str)
{
	if ((*str == '+' || *str == '-' || *str == ' ') && *(str + 1))
		str++;
	while (*str)
	{
		if (!ft_isdigit(*str))
			return (0);
		str++;
	}
	return (1);
}